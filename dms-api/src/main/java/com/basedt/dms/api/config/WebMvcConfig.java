package com.basedt.dms.api.config;

import com.basedt.dms.common.utils.DateTimeUtil;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.core.convert.converter.Converter;

import java.time.LocalDate;
import java.time.LocalDateTime;

@Configuration
public class WebMvcConfig {

    @Bean
    public Converter<String, LocalDateTime> localDateTimeConverter() {
        return new Converter<String, LocalDateTime>() {
            @Override
            public LocalDateTime convert(String source) {
                return DateTimeUtil.toLocalDateTime(source,
                        DateTimeUtil.NORMAL_DATETIME_PATTERN,
                        DateTimeUtil.UTC_DATETIME_MS_WITH_OFFSET_PATTERN,
                        DateTimeUtil.UTC_DATETIME_MS_WITH_ZONE_OFFSET_PATTERN);
            }
        };
    }

    @Bean
    public Converter<String, LocalDate> localDateConverter() {
        return new Converter<String, LocalDate>() {
            @Override
            public LocalDate convert(String source) {
                return DateTimeUtil.toLocalDate(source, DateTimeUtil.NORMAL_DATE_PATTERN);
            }
        };
    }


}
